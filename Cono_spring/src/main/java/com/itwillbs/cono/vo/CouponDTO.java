package com.itwillbs.cono.vo;

public class CouponDTO {
	private String coupon_idx;
	private String member_id;
	private String item_idx;
	private String coupon_title;
	private String coupon_content;
	private String coupon_price;
	private String coupon_status;
	
	public String getCoupon_idx() {
		return coupon_idx;
	}
	public void setCoupon_idx(String coupon_idx) {
		this.coupon_idx = coupon_idx;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getItem_idx() {
		return item_idx;
	}
	public void setItem_idx(String item_idx) {
		this.item_idx = item_idx;
	}
	public String getCoupon_title() {
		return coupon_title;
	}
	public void setCoupon_title(String coupon_title) {
		this.coupon_title = coupon_title;
	}
	public String getCoupon_content() {
		return coupon_content;
	}
	public void setCoupon_content(String coupon_content) {
		this.coupon_content = coupon_content;
	}
	public String getCoupon_price() {
		return coupon_price;
	}
	public void setCoupon_price(String coupon_price) {
		this.coupon_price = coupon_price;
	}
	public String getCoupon_status() {
		return coupon_status;
	}
	public void setCoupon_status(String coupon_status) {
		this.coupon_status = coupon_status;
	}
	
	
	
	
	
}
